# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor 

numero = int(input('Qual o número você deseja?'))

print('O número escolhido foi: {} o seu antecessor é {} e seu sucessor é {}'.format(numero, numero -1, numero + 1))

